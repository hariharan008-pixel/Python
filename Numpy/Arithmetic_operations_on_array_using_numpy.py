import numpy as np

#Addition of array

a=np.array([1,2,3,4,5,6])
b=np.array([8,9,5,4,2,1])

print("Sum : ", a+b)

#Subtraction of array

a=np.array([1,2,3,4,5,6])
b=np.array([8,9,5,4,2,1])

print("Difference : ", a-b)

#Multiplication of array

a=np.array([1,2,3,4,5,6])
b=np.array([8,9,5,4,2,1])

print("Product : ", a*b)

#Division of array

a=np.array([1,2,3,4,5,6])
b=np.array([8,9,5,4,2,1])

print("Divide : ", a/b)

